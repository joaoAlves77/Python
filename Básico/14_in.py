# Operador in
# String, lista(acima não vimos), sequencia range

# com string
frase = "Ola mundo"

if("a" in frase):
    print("Está na frase")
else:
    ("Não está")


# Com sequencia retornada pela função range
sequencia = range(1,10)

if(5 in sequencia):
    print("Esta na sequência")
else:
    print("Não esta na sequencia")