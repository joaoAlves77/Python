# Metodos para strings

frase = "testando string"
print(frase.upper()) # "Upper" deixa todas as letras em maiscular.

frase = "testando string"
print(frase.lower()) # "Lower" deixa todas as letras em minuscula.

frase = "testando string"
print(frase.find("e")) # "find" o programa vai retornar o indice ou a posição.